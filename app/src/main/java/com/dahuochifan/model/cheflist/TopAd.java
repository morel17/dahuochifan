package com.dahuochifan.model.cheflist;

/**
 * Created by Morel on 2015/11/16.
 * top activities on chef list
 */
public class TopAd {
    private String actids, detail, remark, head;

    public String getActids() {
        return actids;
    }

    public void setActids(String actids) {
        this.actids = actids;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }
}
